$(function () {
  var corner1 = L.latLng(59, 19),
    corner2 = L.latLng(72, 32),
    bounds = L.latLngBounds(corner1, corner2);

  var map = L.map("map_canvas")
    .setView([62.349998, 25.28], 6)
    .setMaxBounds(bounds);

  var thm = L.tileLayer(
    "https://www.tilannehuone.fi/tile.php?zoom={z}&x={x}&y={y}&k",
    {
      attribution: "LÃ¤hde: Maanmittauslaitoksen Maastotietokannan aineisto",
      minZoom: 6,
      maxZoom: 15,
      tileSize: 256,
      zoomOffset: 0,
    }
  ).addTo(map);

  var osm = L.tileLayer("https://tile.openstreetmap.org/{z}/{x}/{y}.png", {
    attribution:
      "&copy; <a href='https://www.openstreetmap.org/'>OpenStreetMap</a> and contributors, under an <a href='https://www.openstreetmap.org/copyright' title='ODbL'>open license</a>.",
  }).addTo(map);

  var baseMaps = {
    "Maanmittauslaitoksen taustakartta": thm,
    OpenStreetMap: osm,
  };

  L.control.layers(baseMaps).addTo(map);

  var legend = L.control({ position: "bottomright" });
  function createControl() {
    legend.onAdd = function (map) {
      var div = L.DomUtil.create("div", "tiesitko legend");

      div.innerHTML += '<img src="images/tiesitko.png"">'; // don't forget the break tag

      return div;
    };

    legend.addTo(map);
  }

  map.on("zoomend", onZoomend);
  function onZoomend() {
    if (map.getZoom() <= 10) {
      map.removeControl(legend);
    }
    if (map.getZoom() > 10) {
      createControl();
    }
  }
  var latlng = [62.349998, 25.28];
  var zoom = 6;

  var halymarkers = [];
  map.setView(latlng, zoom);
  for (var i = 0; i < halytykset.length; i++) {
    var h = halytykset[i];
    var addicons = [];

    var hmp = L.popup({ maxWidth: 400, minWidth: 250 });
    hmp.setContent(
      '<div style="margin-right: 15px">' +
        '<table class="kupla">' +
        "<tr>" +
        '<td valign="top" style="padding-right: 10px">' +
        '<img src="https://www.tilannehuone.fi/halytys/kuvat/' +
        h.icon +
        '">' +
        "</td>" +
        "<td>" +
        '<a style="float:right; padding-left: 10px;" href="../tehtava.php?hash=' +
        h.h +
        '"><img border="0" alt="" src="../halytys/kuvat/page.gif"></a><a style="float:right;" onclick="newPopwinInfo(\'halytys_info.php?hash=' +
        h.h +
        '\');return false;" href="../halytys_info.php?hash=' +
        h.h +
        '"><img border="0" alt="" src="/halytys/kuvat/kyna.gif"></a>' +
        h.d +
        "<br>" +
        "<strong>" +
        halytrans[h.p] +
        "</strong><br>" +
        halytrans[h.t] +
        "<br>" +
        h.txt +
        "</td>" +
        "</tr>" +
        "</table>" +
        "</div>" +
        '<div style="clear: both;"></div>'
    );

    if (h["var"][2] === 1) {
      addicons.push(
        L.marker(h.pos, {
          icon: new targBaseIcon({
            iconUrl:
              "https://www.tilannehuone.fi/halytys/kuvat/target-" +
              h.icons +
              ".png",
            iconSize: [h.icons + 4, h.icons + 4],
            iconAnchor: [(h.icons + 4) / 2, (h.icons + 4) / 2],
          }),
        })
          .bindPopup(hmp)
          .addTo(map)
      );
    }

    var hmm = L.marker(h.pos, {
      icon: L.icon({
        iconUrl: "https://www.tilannehuone.fi/halytys/kuvat/" + h.icon,
        iconRetinaUrl: "https://www.tilannehuone.fi/halytys/kuvat/" + h.iconhi,
        iconSize: [h.icons, h.icons],
        iconAnchor: [h.icons / 2, h.icons / 2],
      }),
    })
      .bindPopup(hmp)
      .addTo(map);

    //infoic, staric, targic, kuvaic

    if (h["var"][0] === 1) {
      addicons.push(
        L.marker(h.pos, {
          icon: new infoBaseIcon({
            iconAnchor: [
              8 - (h.icons / 2) * 0.7 - 3,
              8 + (h.icons / 2) * 0.7 + 3,
            ],
          }),
        })
          .bindPopup(hmp)
          .addTo(map)
      );
    }

    if (h["var"][1] === 1) {
      addicons.push(
        L.marker(h.pos, {
          icon: new starBaseIcon({
            iconAnchor: [
              8 + (h.icons / 2) * 0.7 + 3,
              8 + (h.icons / 2) * 0.7 + 3,
            ],
          }),
        })
          .bindPopup(hmp)
          .addTo(map)
      );
    }

    if (h["var"][3] === 1) {
      addicons.push(
        L.marker(h.pos, {
          icon: new kuvaBaseIcon({
            iconAnchor: [
              5 - (h.icons / 2) * 0.7 - 3,
              7 - (h.icons / 2) * 0.7 - 3,
            ],
          }),
        })
          .bindPopup(hmp)
          .addTo(map)
      );
    }

    halymarkers.push({ mainmark: hmm, popup: hmp, addicons: addicons });
  }
});

